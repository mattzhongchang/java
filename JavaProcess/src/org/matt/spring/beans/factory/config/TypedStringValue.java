package org.matt.spring.beans.factory.config;

public class TypedStringValue {

	private String value;

	public TypedStringValue(String value) {
		super();
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	
	
    
}
